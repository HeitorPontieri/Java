public class CalculadoraObj {
    // Atributos
    public double valor1;
    public double valor2;

    public double somar(){
        return valor1+valor2;
    }
    public double subtrair(){
        return valor1 - valor2;
    }
    public double multiplicar(){
        return valor1 * valor2;
    }
    public double dividir(){
        return valor1/valor2; 
    }
}
